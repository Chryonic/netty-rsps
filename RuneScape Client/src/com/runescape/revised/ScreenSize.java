package com.runescape.revised;

/**
 * The type of screen size.
 * 
 * @author josh
 *
 */
public enum ScreenSize {

	/**
	 * The normal screen size.
	 */
	NORMAL,
	
	/**
	 * The mid screen size.
	 */
	MID,
	
	/**
	 * The windowed fullscreen that only
	 * stops short of the real fullscreen.
	 */
	WINDOWED_FULL,
	
	/**
	 * The full screen size.
	 */
	FULLSCREEN;
}